let numero = 2;
let numero2 = 4;
let aux;

// Soma
console.log(numero + numero2);
// Subtração
console.log(numero - numero2);
// Multiplicação
console.log(numero * numero2);
// Divisão
console.log(numero / numero2);
// Resto da divisão
console.log(numero % numero2);
// Tipos de =
// Atribuição
numero = 25
console.log(numero)
// Comparação porém ignora o tipo da variavel
aux = numero == "25";
console.log(aux)
//Comparação porém se preocupa com os tipos da variaveis
aux = numero === "25";
console.log(aux);